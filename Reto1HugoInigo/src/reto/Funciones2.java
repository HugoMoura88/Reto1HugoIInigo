public class Funciones2 {

	public static int multiplicar(int nu1, int nu2) {
		return nu1*nu2;
	}
	public static int dividir(int nu1, int nu2) {
		return nu1/nu2;
	}
	public static boolean esPositivo(int nu1) {
		return (nu1>=0);
	}
	public static boolean esNegativo(int nu1) {
		return (nu1<0);
	}
	
}
